# Operações aritméticas

"""
    +   Soma
    -   Subtração
    *   Multiplicação
    /   Divisão
    //  Divisão inteira
    %   Resto da divisão ou módulo
    **  Potência
"""

num1 = 5
num2 = 3

print(num1 + num2)
print(num1 - num2)
print(num1 / num2)
print(num1 // num2)
print(num1 % num2)
print(num1 ** num2)

# Operadores booleanos

age1 = 15
age2 = 30
height1 = 1.77
height2 = 1.65
height3 = height2

print(age1 > age2)          # False
print(age1 <= age2)         # True
print("Python" == "python") # False
print("banana" != "apple")  # True
print(height1 >= height2)   # True
print(height2 > height3)    # False